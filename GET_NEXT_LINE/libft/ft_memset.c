#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*str;

	if (len > 0)
	{
		str = (char*)b;
		while (len--)
		{
			*str = (char)c;
			str++;
		}
	}
	return (b);
}
